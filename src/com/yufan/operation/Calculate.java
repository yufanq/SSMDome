package com.yufan.operation;
/**
 * 
* @ClassName: Calculate  
* @Description:  实现两个数的加减法
* @author 雨ゆこ
* @date 2018年3月7日  
*  
*
 */
public class Calculate {
	/**
	 * 
	* @Title: add  
	* @Description:加法运算  
	* @param @param n1
	* @param @param n2
	* @param @return    参数  
	* @return double    返回类型  
	* @throws
	 */
	public double add(double n1, double n2){
		return n1 + n2;
	}
	/**
	 * 
	* @Title: sub  
	* @Description:  减法运算 
	* @param @param n1
	* @param @param n2
	* @param @return    参数  
	* @return double    返回类型  
	* @throws
	 */
	public double sub(double n1, double n2){
		return n1 - n2;
	}
}
